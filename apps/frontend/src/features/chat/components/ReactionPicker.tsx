const ReactionPicker: React.FC<ReactionPickerProps> = ({
  onSelect,
  trigger,
  position = 'top',
  align = 'center',
  className,
}) => {
  const [isOpen, setIsOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState<keyof typeof REACTION_CATEGORIES>('recent');
  const [searchQuery, setSearchQuery] = useState('');
  const pickerRef = useRef<HTMLDivElement>(null);
  const triggerRef = useRef<HTMLButtonElement>(null);

  // Close picker on outside click
  const isTestEnv = (process.env as any).JEST_TEST_ENV === 'true';

  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (
        pickerRef.current &&
        !pickerRef.current.contains(e.target as Node) &&
        triggerRef.current &&
        !triggerRef.current.contains(e.target as Node)
      ) {
        setIsOpen(false);
      }
    };

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside);
    }

    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [isOpen]);

  // Filter emojis based on search
  const getFilteredEmojis = () => {
    if (!searchQuery) {
      return REACTION_CATEGORIES[activeCategory];
    }

    // Search across all categories
    const allEmojis = Object.values(REACTION_CATEGORIES).flat();
    return allEmojis.filter(emoji => 
      emoji.toLowerCase().includes(searchQuery.toLowerCase())
    );
  };

  const handleSelect = (emoji: string) => {
    onSelect(emoji);
    setIsOpen(false);
    setSearchQuery('');
  };

  const getPositionClasses = () => {
    const positions = {
      top: 'bottom-full mb-2',
      bottom: 'top-full mt-2',
      left: 'right-full mr-2',
      right: 'left-full ml-2',
    };

    const alignments = {
      start: position === 'top' || position === 'bottom' ? 'left-0' : 'top-0',
      center: position === 'top' || position === 'bottom' ? 'left-1/2 -translate-x-1/2' : 'top-1/2 -translate-y-1/2',
      end: position === 'top' || position === 'bottom' ? 'right-0' : 'bottom-0',
    };

    return `${positions[position]} ${alignments[align]}`;
  };

  const categoryIcons: Record<keyof typeof REACTION_CATEGORIES, string> = {
    recent: 'ğŸ•',
    smileys: 'ğŸ˜€',
    gestures: 'ğŸ‘‹',
    hearts: 'ğŸ’–',
    animals: 'ğŸ¶',
    food: 'ğŸ',
    activities: 'âš½',
    objects: 'ğŸ’¡',
    symbols: 'ğŸ”£',
  };


  return (
    <div className="relative inline-block">
      {/* Trigger */}
      {trigger ? (
        React.isValidElement(trigger)
          ? React.cloneElement(trigger as any, {
              onClick: (e: any) => {
                (trigger as any).props?.onClick?.(e);
                setIsOpen((prev) => !prev);
              },
              ref: triggerRef as any,
            })
          : (
            <div ref={triggerRef as any} onClick={() => setIsOpen(!isOpen)}>{trigger}</div>
          )
      ) : (
        <Button
          ref={triggerRef}
          size="sm"
          variant="ghost"
          onClick={() => setIsOpen(!isOpen)}
          className="h-8 w-8 p-0"
        >
          <Smile className="h-4 w-4" />
        </Button>
      )}

      {/* Picker */}
      {isOpen && (
        <div
          ref={pickerRef}
          className={cn(
            "absolute z-50 bg-background border rounded-lg shadow-lg",
            "w-80 max-h-96 overflow-hidden",
            getPositionClasses(),
            className
          )}
        >
          {/* Quick reactions */}
          <div className="border-b p-2">
            <div className="flex gap-1">
              {QUICK_REACTIONS.map((emoji) => (
                <button
                  key={emoji}
                  aria-label={emoji}
                  className="h-8 w-8 p-0 text-base hover:bg-muted rounded-md"
                  onClick={() => handleSelect(emoji)}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>

          {/* Search */}
          <div className="border-b p-2">
            <input
              type="text"
              placeholder="Search emoji..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className={cn(
                "w-full px-2 py-1 text-sm rounded border",
                "focus:outline-none focus:ring-2 focus:ring-ring"
              )}
            />
          </div>

          {/* Categories */}
          {!searchQuery && (
            <div className="flex border-b overflow-x-auto">
              {Object.entries(categoryIcons).map(([category, icon]) => (
                <button
                  key={category}
                  className={cn(
                    "h-10 px-3 rounded-none",
                    activeCategory === category && "bg-muted"
                  )}
                  onClick={() => setActiveCategory(category as keyof typeof REACTION_CATEGORIES)}
                >
                  <span className="text-base">{icon}</span>
                </button>
              ))}
            </div>
          )}

          {/* Emoji grid (exclude duplicates already shown in quick reactions) */}
          <div className="p-2 h-60 overflow-y-auto">
            {searchQuery && getFilteredEmojis().length === 0 ? (
              <div className="text-center text-sm text-muted-foreground py-8">
                No emojis found
              </div>
            ) : (
              <div className="grid grid-cols-8 gap-1">
                {getFilteredEmojis()
                  .filter((emoji) => !QUICK_REACTIONS.includes(emoji))
                  .map((emoji, index) => (
                  <button
                    key={`${emoji}-${index}`}
                    aria-label={emoji}
                    className="h-8 w-8 p-0 text-base hover:bg-muted rounded-md"
                    onClick={() => handleSelect(emoji)}
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            )}
          </div>
        </div>
      )}
    </div>
  );
};

export default ReactionPicker;